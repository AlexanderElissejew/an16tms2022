public class Homework {
    public static void main(String[] args) {
        //        вывести на консоль литералы следующих видов:
        //        логический
        System.out.println("Logicheskiy: " + true);
        //        строковый
        System.out.println("Strokowiy");
        //        символьный
        System.out.println("Simwolniy: " + 'x');
        //        целочисленный 2-й
        System.out.println("Celochislenniy 2-y: " + 0b01);
        //        целочисленный 8-й
        System.out.println("Celochislenniy 8-y: " + 010);
        //        целочисленный 10-й
        System.out.println("Celochislenniy 10-y: " + 8);
        //        целочисленный 16-й
        System.out.println("Celochislenniy 16-y: " + 0x10);
        //        литерал типа float
        System.out.println("Float: " + 2e-2f);
        //        литерал типа double.
        System.out.println("Double: " + 1.2);
    }
}
